#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *tmp;

	if (lst && f)
	{
		tmp = lst->next;
		f(lst);
		ft_lstiter(tmp, f);
	}
}
